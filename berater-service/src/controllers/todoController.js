// @desc    Get all todos (including reminders converted to TODOs)
// @route   GET /api/todos
// @access  Private
exports.getTodos = async (req, res, next) => {
  try {
    const { status, priority, search, page = 1, limit = 50 } = req.query;

    const filter = { beraterId: req.user._id, isSupportTicket: { $ne: true } };

    if (status) filter.status = status;
    if (priority) filter.priority = priority;
    if (search) {
      filter.$or = [
        { title: { $regex: search, $options: 'i' } },
        { description: { $regex: search, $options: 'i' } }
      ];
    }

    // Fetch regular TODOs
    const todos = await Todo.find(filter)
      .populate('relatedCustomerId', 'firstName lastName customerNumber')
      .populate('relatedContractId', 'contractNumber startDate endDate')
      .populate('relatedMeterId', 'meterNumber type')
      .lean();

    // Fetch Reminders and convert them to TODO format
    const reminderFilter = { beraterId: req.user._id };

    // Map status filter to reminder status
    if (status) {
      if (status === 'completed') {
        reminderFilter.status = 'done';
      } else if (status === 'open' || status === 'in_progress') {
        reminderFilter.status = 'open';
      }
    }

    const reminders = await Reminder.find(reminderFilter)
      .populate({
        path: 'contractId',
        populate: [
          { path: 'customerId', select: 'firstName lastName customerNumber' },
          { path: 'supplierId', select: 'name shortName' }
        ]
      })
      .lean();

    // Convert reminders to TODO format
    const reminderTodos = reminders.map(reminder => {
      const daysMap = {
        '30days': 30,
        '60days': 60,
        '90days': 90
      };
      const days = daysMap[reminder.reminderType] || 90;

      const priorityMap = {
        '30days': 'high',
        '60days': 'medium',
        '90days': 'low'
      };
      const reminderPriority = priorityMap[reminder.reminderType] || 'low';

      // Skip if priority filter doesn't match
      if (priority && reminderPriority !== priority) {
        return null;
      }

      const statusMap = {
        'open': 'open',
        'done': 'completed',
        'ignored': 'completed'
      };

      const title = `Vertrag läuft in ${days} Tagen ab`;
      const customerName = reminder.contractId?.customerId
        ? `${reminder.contractId.customerId.firstName} ${reminder.contractId.customerId.lastName}`
        : 'Unbekannt';
      const supplierName = reminder.contractId?.supplierId?.name || 'Unbekannt';

      const description = `Erinnerung: Der Vertrag von ${customerName} bei ${supplierName} läuft bald ab. Bitte rechtzeitig Verlängerung oder Kündigung vorbereiten.`;

      // Apply search filter
      if (search) {
        const searchLower = search.toLowerCase();
        if (!title.toLowerCase().includes(searchLower) &&
            !description.toLowerCase().includes(searchLower)) {
          return null;
        }
      }

      return {
        _id: `reminder_${reminder._id}`,
        title,
        description,
        status: statusMap[reminder.status] || 'open',
        priority: reminderPriority,
        dueDate: reminder.dueDate,
        relatedCustomerId: reminder.contractId?.customerId || null,
        relatedContractId: reminder.contractId || null,
        relatedMeterId: null,
        isAutoGenerated: true,
        autoGenerationType: 'reminder_due',
        isReminder: true,
        originalReminderId: reminder._id,
        note: reminder.note,
        createdAt: reminder.createdAt,
        updatedAt: reminder.updatedAt,
        beraterId: reminder.beraterId
      };
    }).filter(Boolean); // Remove null entries from filtering

    // Combine both arrays
    const allTodos = [...todos, ...reminderTodos];

    // Sort by dueDate, then createdAt
    allTodos.sort((a, b) => {
      if (a.dueDate && b.dueDate) {
        return new Date(a.dueDate) - new Date(b.dueDate);
      }
      if (a.dueDate) return -1;
      if (b.dueDate) return 1;
      return new Date(b.createdAt) - new Date(a.createdAt);
    });

    // Apply pagination to combined results
    const skip = (page - 1) * limit;
    const total = allTodos.length;
    const paginatedTodos = allTodos.slice(skip, skip + parseInt(limit));

    res.status(200).json({
      success: true,
      data: paginatedTodos,
      pagination: {
        page: parseInt(page),
        limit: parseInt(limit),
        total,
        pages: Math.ceil(total / limit)
      }
    });
  } catch (error) {
    next(error);
  }
};

