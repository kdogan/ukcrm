// @desc    Create todo
// @route   POST /api/todos
// @access  Private
exports.createTodo = async (req, res, next) => {
  try {
    const todoData = {
      ...req.body,
      beraterId: req.user._id,
      isAutoGenerated: false
    };

    const todo = await Todo.create(todoData);

    // Wenn ein TODO für einen Vertrag erstellt wird, lösche existierende offene Reminders
    // um Duplikate zu vermeiden
    if (todo.relatedContractId) {
      await Reminder.updateMany(
        {
          beraterId: req.user._id,
          contractId: todo.relatedContractId,
          status: 'open'
        },
        {
          status: 'ignored',
          note: 'Automatisch ignoriert - Manuelles TODO wurde erstellt'
        }
      );
    }

    const populatedTodo = await Todo.findById(todo._id)
      .populate('relatedCustomerId', 'firstName lastName customerNumber')
      .populate('relatedContractId', 'contractNumber startDate endDate')
      .populate('relatedMeterId', 'meterNumber type');

    res.status(201).json({
      success: true,
      data: populatedTodo
    });
  } catch (error) {
    next(error);
  }
};

// @desc    Update todo (or reminder-based todo)
// @route   PUT /api/todos/:id
// @access  Private
exports.updateTodo = async (req, res, next) => {
  try {
    // Check if this is a reminder-based TODO
    if (req.params.id.startsWith('reminder_')) {
      const reminderId = req.params.id.replace('reminder_', '');

      const reminder = await Reminder.findOne({
        _id: reminderId,
        beraterId: req.user._id
      });

      if (!reminder) {
        return res.status(404).json({
          success: false,
          message: 'Erinnerung nicht gefunden'
        });
      }

      // Update reminder status based on TODO status
      if (req.body.status) {
        const statusMap = {
          'open': 'open',
          'in_progress': 'open',
          'completed': 'done'
        };
        reminder.status = statusMap[req.body.status] || 'open';
      }

      // Update note if provided
      if (req.body.note !== undefined) {
        reminder.note = req.body.note;
      }

      await reminder.save();

      // Return the reminder as a TODO
      const populatedReminder = await Reminder.findById(reminder._id)
        .populate({
          path: 'contractId',
          populate: [
            { path: 'customerId', select: 'firstName lastName customerNumber' },
            { path: 'supplierId', select: 'name shortName' }
          ]
        });

      const daysMap = {
        '30days': 30,
        '60days': 60,
        '90days': 90
      };
      const days = daysMap[populatedReminder.reminderType] || 90;

      const priorityMap = {
        '30days': 'high',
        '60days': 'medium',
        '90days': 'low'
      };

      const statusMap = {
        'open': 'open',
        'done': 'completed',
        'ignored': 'completed'
      };

      const customerName = populatedReminder.contractId?.customerId
        ? `${populatedReminder.contractId.customerId.firstName} ${populatedReminder.contractId.customerId.lastName}`
        : 'Unbekannt';
      const supplierName = populatedReminder.contractId?.supplierId?.name || 'Unbekannt';

      const reminderTodo = {
        _id: `reminder_${populatedReminder._id}`,
        title: `Vertrag läuft in ${days} Tagen ab`,
        description: `Erinnerung: Der Vertrag von ${customerName} bei ${supplierName} läuft bald ab. Bitte rechtzeitig Verlängerung oder Kündigung vorbereiten.`,
        status: statusMap[populatedReminder.status] || 'open',
        priority: priorityMap[populatedReminder.reminderType] || 'low',
        dueDate: populatedReminder.dueDate,
        relatedCustomerId: populatedReminder.contractId?.customerId || null,
        relatedContractId: populatedReminder.contractId || null,
        relatedMeterId: null,
        isAutoGenerated: true,
        autoGenerationType: 'reminder_due',
        isReminder: true,
        originalReminderId: populatedReminder._id,
        note: populatedReminder.note,
        createdAt: populatedReminder.createdAt,
        updatedAt: populatedReminder.updatedAt,
        beraterId: populatedReminder.beraterId
      };

      return res.status(200).json({
        success: true,
        data: reminderTodo
      });
    }

    // Regular TODO update
    const todo = await Todo.findOne({
      _id: req.params.id,
      beraterId: req.user._id
    });

    if (!todo) {
      return res.status(404).json({
        success: false,
        message: 'TODO nicht gefunden'
      });
    }

    // Erlaubte Felder
    const allowedFields = [
      'title',
      'description',
      'status',
      'priority',
      'dueDate',
      'relatedCustomerId',
      'relatedContractId',
      'relatedMeterId'
    ];

    allowedFields.forEach(field => {
      if (req.body[field] !== undefined) {
        todo[field] = req.body[field];
      }
    });

    await todo.save();

    const populatedTodo = await Todo.findById(todo._id)
      .populate('relatedCustomerId', 'firstName lastName customerNumber')
      .populate('relatedContractId', 'contractNumber startDate endDate')
      .populate('relatedMeterId', 'meterNumber type');

    res.status(200).json({
      success: true,
      data: populatedTodo
    });
  } catch (error) {
    next(error);
  }
};

// @desc    Delete todo (or reminder-based todo)
// @route   DELETE /api/todos/:id
// @access  Private
exports.deleteTodo = async (req, res, next) => {
  try {
    // Check if this is a reminder-based TODO
    if (req.params.id.startsWith('reminder_')) {
      const reminderId = req.params.id.replace('reminder_', '');

      const reminder = await Reminder.findOne({
        _id: reminderId,
        beraterId: req.user._id
      });

      if (!reminder) {
        return res.status(404).json({
          success: false,
          message: 'Erinnerung nicht gefunden'
        });
      }

      // Mark reminder as ignored instead of deleting
      reminder.status = 'ignored';
      await reminder.save();

      return res.status(200).json({
        success: true,
        message: 'Erinnerung ignoriert'
      });
    }

    // Regular TODO delete
    const todo = await Todo.findOneAndDelete({
      _id: req.params.id,
      beraterId: req.user._id
    });

    if (!todo) {
      return res.status(404).json({
        success: false,
        message: 'TODO nicht gefunden'
      });
    }

    res.status(200).json({
      success: true,
      message: 'TODO gelöscht'
    });
  } catch (error) {
    next(error);
  }
};

