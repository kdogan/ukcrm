// @desc    Respond to support ticket / Close ticket
// @route   PUT /api/todos/support-ticket/:id/respond
// @access  Private (Berater can close own tickets, Superadmin can respond and change all statuses)
exports.respondToSupportTicket = async (req, res, next) => {
  try {
    const { response, status } = req.body;

    // At least response or status must be provided
    if (!response && !status) {
      return res.status(400).json({
        success: false,
        message: 'Antwort oder Status ist erforderlich'
      });
    }

    const ticket = await Todo.findOne({
      _id: req.params.id,
      isSupportTicket: true
    });

    if (!ticket) {
      return res.status(404).json({
        success: false,
        message: 'Support-Ticket nicht gefunden'
      });
    }

    const isSuperAdmin = req.user.role === 'superadmin';
    const isTicketOwner = ticket.beraterId.toString() === req.user._id.toString();

    // Check permissions
    if (!isSuperAdmin && !isTicketOwner) {
      return res.status(403).json({
        success: false,
        message: 'Sie können nur Ihre eigenen Tickets verwalten'
      });
    }

    // Berater can change status to open, in_progress, or completed
    if (!isSuperAdmin && status && !['open', 'in_progress', 'completed'].includes(status)) {
      return res.status(403).json({
        success: false,
        message: 'Ungültiger Status'
      });
    }

    // Update response if provided
    if (response && response.trim().length > 0) {
      if (isSuperAdmin) {
        // Superadmin response
        ticket.adminResponse = response;
        ticket.adminResponseAt = new Date();
        ticket.respondedBy = req.user._id;
      } else if (isTicketOwner) {
        // Berater can add/update their own response
        ticket.beraterResponse = response;
        ticket.beraterResponseAt = new Date();
      }
    }

    // Update status if provided
    if (status) {
      ticket.status = status;
    }

    await ticket.save();

    const populatedTicket = await Todo.findById(ticket._id)
      .populate('beraterId', 'firstName lastName email')
      .populate('respondedBy', 'firstName lastName');

    res.status(200).json({
      success: true,
      data: populatedTicket,
      message: 'Erfolgreich aktualisiert'
    });
  } catch (error) {
    next(error);
  }
};

// @desc    Get unread support ticket count for badge
// @route   GET /api/todos/support-ticket-count
// @access  Private
exports.getSupportTicketBadgeCount = async (req, res, next) => {
  try {
    const isSuperAdmin = req.user.role === 'superadmin';
    let count = 0;

    if (isSuperAdmin) {
      // Superadmin: Zähle offene Tickets ohne Admin-Antwort (noch zu bearbeiten)
      count = await Todo.countDocuments({
        isSupportTicket: true,
        status: { $in: ['open', 'in_progress'] },
        adminResponse: { $in: [null, ''] }
      });
    } else {
      // Berater: Zähle eigene Tickets mit Admin-Antwort, die noch nicht als gelesen markiert wurden
      // (status ist nicht completed und adminResponse existiert)
      count = await Todo.countDocuments({
        beraterId: req.user._id,
        isSupportTicket: true,
        status: { $ne: 'completed' },
        adminResponse: { $nin: [null, ''] },
        adminResponseReadByBerater: { $ne: true }
      });
    }

    res.status(200).json({
      success: true,
      count
    });
  } catch (error) {
    next(error);
  }
};

// @desc    Mark admin response as read by berater
// @route   PUT /api/todos/support-ticket/:id/mark-read
// @access  Private (Berater only)
exports.markSupportTicketAsRead = async (req, res, next) => {
  try {
    const ticket = await Todo.findOne({
      _id: req.params.id,
      beraterId: req.user._id,
      isSupportTicket: true
    });

    if (!ticket) {
      return res.status(404).json({
        success: false,
        message: 'Support-Ticket nicht gefunden'
      });
    }

    ticket.adminResponseReadByBerater = true;
    await ticket.save();

    res.status(200).json({
      success: true,
      message: 'Ticket als gelesen markiert'
    });
  } catch (error) {
    next(error);
  }
};

// @desc    Get support ticket image
// @route   GET /api/todos/support-ticket/image/:ticketId/:filename
// @access  Private
exports.getSupportTicketImage = async (req, res, next) => {
  try {
    const { ticketId, filename } = req.params;

    const ticket = await Todo.findById(ticketId);

    if (!ticket || !ticket.isSupportTicket) {
      return res.status(404).send('Support-Ticket nicht gefunden');
    }

    // Check if user has access to this ticket
    if (req.user.role !== 'superadmin' && ticket.beraterId.toString() !== req.user._id.toString()) {
      return res.status(403).send('Keine Berechtigung');
    }

    const image = ticket.ticketImages.find(img => img.filename === filename);

    if (!image) {
      return res.status(404).send('Bild nicht gefunden');
    }

    // Set CORS headers to allow browser to load the image
    res.header('Access-Control-Allow-Origin', req.headers.origin || '*');
    res.header('Access-Control-Allow-Credentials', 'true');
    res.header('Cross-Origin-Resource-Policy', 'cross-origin');

    // Set content type
    res.contentType(image.mimeType);

    // Send the file
    res.sendFile(path.resolve(image.path));
  } catch (error) {
    res.status(500).send('Fehler beim Laden des Bildes');
  }
};
